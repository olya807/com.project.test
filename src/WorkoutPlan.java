public class WorkoutPlan {

    public WorkoutPlan() {
    }

    public static void main(String[] args) {
        String[] days = {
                "День 1: Грудь и трицепсы",
                "День 2: Спина и бицепсы",
                "День 3: Отдых или легкая кардио-тренировка",
                "День 4: Ноги",
                "День 5: Плечи и пресс",
                "День 6: Отдых или легкая кардио-тренировка",
                "День 7: Полное тело (легкая тренировка)"
        };

        String[][] exercises = {
                { // День 1
                        "Разминка: 5-10 минут легкого кардио",
                        "Жим гантелей лежа: 3 подхода по 10-12 повторений",
                        "Разведение гантелей лежа: 3 подхода по 10-12 повторений",
                        "Французский жим с гантелями: 3 подхода по 10-12 повторений",
                        "Разгибание рук на блоке: 3 подхода по 12-15 повторений"
                },
                { // День 2
                        "Разминка: 5-10 минут легкого кардио",
                        "Тяга верхнего блока к груди: 3 подхода по 10-12 повторений",
                        "Тяга гантели в наклоне: 3 подхода по 10-12 повторений",
                        "Сгибание рук с гантелями стоя: 3 подхода по 10-12 повторений",
                        "Концентрированное сгибание на бицепс: 3 подхода по 12-15 повторений"
                },
                { // День 3
                        "Легкий кардио: 20-30 минут ходьбы или легкого бега"
                },
                { // День 4
                        "Разминка: 5-10 минут легкого кардио",
                        "Приседания с гантелями: 3 подхода по 10-12 повторений",
                        "Выпады с гантелями: 3 подхода по 10-12 повторений на каждую ногу",
                        "Жим ногами в тренажере: 3 подхода по 10-12 повторений",
                        "Подъемы на носки (икры): 3 подхода по 15-20 повторений"
                },
                { // День 5
                        "Разминка: 5-10 минут легкого кардио",
                        "Жим гантелей сидя: 3 подхода по 10-12 повторений",
                        "Разведение гантелей в стороны: 3 подхода по 10-12 повторений",
                        "Подъемы гантелей перед собой: 3 подхода по 10-12 повторений",
                        "Подъемы корпуса на пресс: 3 подхода по 15-20 повторений",
                        "Планка: 3 подхода по 30-60 секунд"
                },
                { // День 6
                        "Легкий кардио: 20-30 минут ходьбы или легкого бега"
                },
                { // День 7
                        "Разминка: 5-10 минут легкого кардио",
                        "Жим гантелей лежа: 2 подхода по 10-12 повторений",
                        "Тяга верхнего блока к груди: 2 подхода по 10-12 повторений",
                        "Приседания с гантелями: 2 подхода по 10-12 повторений",
                        "Жим гантелей сидя: 2 подхода по 10-12 повторений",
                        "Подъемы корпуса на пресс: 2 подхода по 15-20 повторений"
                }
        };

        for (int i = 0; i < days.length; i++) {
            System.out.println(days[i]);
            for (int j = 0; j < exercises[i].length; j++) {
                System.out.println(" - " + exercises[i][j]);
            }
            System.out.println();
        }
    }
}
